// Generates the committed Python-version snapshots from python-build's
// date-keyed manifest.json (the single source of truth):
//
//   * lib/src/python_versions.dart        — Dart consts for the CLI commands
//   * <native package>/python_versions.properties — KEY=VALUE tables the
//     Android/Darwin/Linux/Windows build configs read at build time
//
// Usage (run from the serious_python package dir):
//   dart run serious_python:gen_version_tables                 # re-fetch the pinned release
//   dart run serious_python:gen_version_tables --release-date 20260615
//   dart run serious_python:gen_version_tables --manifest path/to/manifest.json
//
// CI runs the no-arg form and `git diff --exit-code`s the result to guard drift.
import 'dart:convert';
import 'dart:io';

import 'package:http/http.dart' as http;
import 'package:path/path.dart' as p;

const _releaseUrlBase =
    'https://github.com/flet-dev/python-build/releases/download';
const _generatedDartFile = 'lib/src/python_versions.dart';

// Native packages (siblings under src/) that read python_versions.properties.
const _nativePackageDirs = <String>[
  '../serious_python_android/android',
  '../serious_python_darwin/darwin',
  '../serious_python_linux/linux',
  '../serious_python_windows/windows',
];

Future<void> main(List<String> args) async {
  String? releaseDate;
  String? manifestPath;
  for (var i = 0; i < args.length; i++) {
    switch (args[i]) {
      case '--release-date':
        releaseDate = args[++i];
        break;
      case '--manifest':
        manifestPath = args[++i];
        break;
      default:
        stderr.writeln('Unknown argument: ${args[i]}');
        exit(2);
    }
  }

  final Map<String, dynamic> manifest;
  if (manifestPath != null) {
    manifest = jsonDecode(await File(manifestPath).readAsString())
        as Map<String, dynamic>;
  } else {
    releaseDate ??= _readPinnedReleaseDate();
    if (releaseDate == null) {
      stderr.writeln('Pass --release-date YYYYMMDD (or --manifest PATH) — no '
          'pinned release found in $_generatedDartFile.');
      exit(2);
    }
    final url = '$_releaseUrlBase/$releaseDate/manifest.json';
    final resp = await http.get(Uri.parse(url));
    if (resp.statusCode != 200) {
      stderr.writeln('Failed to fetch $url (HTTP ${resp.statusCode}).');
      exit(1);
    }
    manifest = jsonDecode(resp.body) as Map<String, dynamic>;
  }

  final release = (manifest['release'] ?? releaseDate) as String?;
  if (release == null) {
    stderr.writeln('Manifest has no "release" field; pass --release-date.');
    exit(2);
  }
  final defaultPython = manifest['default_python_version'] as String;
  final dartBridge = manifest['dart_bridge_version'] as String;
  final pythons = (manifest['pythons'] as Map).cast<String, dynamic>();
  final shorts = pythons.keys.toList()..sort();

  await File(_generatedDartFile)
      .writeAsString(_dartFile(release, defaultPython, dartBridge, pythons, shorts));

  final props = _propertiesFile(release, defaultPython, dartBridge, pythons, shorts);
  for (final dir in _nativePackageDirs) {
    await File(p.join(dir, 'python_versions.properties')).writeAsString(props);
  }

  stdout.writeln(
      'Generated version tables for release $release (${shorts.join(", ")}).');
}

String? _readPinnedReleaseDate() {
  final f = File(_generatedDartFile);
  if (!f.existsSync()) return null;
  final m = RegExp(r'''pythonReleaseDate\s*=\s*["'](\d+)["']''')
      .firstMatch(f.readAsStringSync());
  return m?.group(1);
}

String _genHeader(String release) => '''
// GENERATED by `dart run serious_python:gen_version_tables` from python-build's
// manifest.json (release $release). Do not edit by hand — edit python-build's
// manifest.json, cut a release, bump `pythonReleaseDate`, and regenerate.
''';

String _dartFile(String release, String defaultPython, String dartBridge,
    Map<String, dynamic> pythons, List<String> shorts) {
  final b = StringBuffer(_genHeader(release))
    ..writeln()
    ..writeln('const pythonVersionEnvironmentVariable = "SERIOUS_PYTHON_VERSION";')
    ..writeln(
        'const pythonFullVersionEnvironmentVariable = "SERIOUS_PYTHON_FULL_VERSION";')
    ..writeln(
        'const pythonDistReleaseEnvironmentVariable = "SERIOUS_PYTHON_DIST_RELEASE";')
    ..writeln(
        'const pythonBuildDateEnvironmentVariable = "SERIOUS_PYTHON_BUILD_DATE";')
    ..writeln(
        'const pyodideVersionEnvironmentVariable = "SERIOUS_PYTHON_PYODIDE_VERSION";')
    ..writeln('const dartBridgeVersionEnvironmentVariable = "DART_BRIDGE_VERSION";')
    ..writeln()
    ..writeln('/// python-build release the bundled runtimes come from (YYYYMMDD).')
    ..writeln('const pythonReleaseDate = "$release";')
    ..writeln('const dartBridgeVersion = "$dartBridge";')
    ..writeln('const defaultPythonVersion = "$defaultPython";')
    ..writeln()
    ..writeln('class PythonRelease {')
    ..writeln('  const PythonRelease({')
    ..writeln('    required this.standaloneVersion,')
    ..writeln('    required this.standaloneReleaseDate,')
    ..writeln('    required this.pyodideVersion,')
    ..writeln('    required this.pyodidePlatformTag,')
    ..writeln('    required this.androidAbis,')
    ..writeln('    required this.prerelease,')
    ..writeln('  });')
    ..writeln()
    ..writeln('  final String standaloneVersion;')
    ..writeln('  final String standaloneReleaseDate;')
    ..writeln('  final String pyodideVersion;')
    ..writeln('  final String pyodidePlatformTag;')
    ..writeln('')
    ..writeln('  /// Android ABIs python-build publishes distributions for')
    ..writeln('  /// this minor. 32-bit Android was dropped in 3.13 (PEP 738);')
    ..writeln('  /// only 3.12 still carries `armeabi-v7a`.')
    ..writeln('  final List<String> androidAbis;')
    ..writeln('')
    ..writeln('  final bool prerelease;')
    ..writeln('}')
    ..writeln()
    ..writeln('const pythonReleases = <String, PythonRelease>{');
  for (final s in shorts) {
    final r = pythons[s] as Map<String, dynamic>;
    final abis = (r['android_abis'] as List).cast<String>();
    final abisLiteral = abis.map((a) => '"$a"').join(', ');
    b
      ..writeln('  "$s": PythonRelease(')
      ..writeln('    standaloneVersion: "${r['full_version']}",')
      ..writeln('    standaloneReleaseDate: "${r['standalone_release_date']}",')
      ..writeln('    pyodideVersion: "${r['pyodide_version']}",')
      ..writeln('    pyodidePlatformTag: "${r['pyodide_platform_tag']}",')
      ..writeln('    androidAbis: [$abisLiteral],')
      ..writeln('    prerelease: ${r['prerelease'] == true},')
      ..writeln('  ),');
  }
  b.writeln('};');
  return b.toString();
}

String _propertiesFile(String release, String defaultPython, String dartBridge,
    Map<String, dynamic> pythons, List<String> shorts) {
  final b = StringBuffer()
    ..writeln('# GENERATED by `dart run serious_python:gen_version_tables` from')
    ..writeln('# python-build manifest.json (release $release). Do not edit by hand.')
    ..writeln('default_python_version=$defaultPython')
    ..writeln('dart_bridge_version=$dartBridge')
    ..writeln('python_build_release_date=$release');
  for (final s in shorts) {
    final r = pythons[s] as Map<String, dynamic>;
    b.writeln('$s.full_version=${r['full_version']}');
    final abis = (r['android_abis'] as List).cast<String>();
    b.writeln('$s.android_abis=${abis.join(",")}');
  }
  return b.toString();
}
